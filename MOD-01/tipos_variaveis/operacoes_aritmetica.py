# soma +

soma = 1 + 1 
print(soma)


# subtração - 
subtracao = 1 -1 
print(subtracao)

# Multiplicação
multiplicacao = 1 * 1
print(multiplicacao)

# Divisão 
divisao = 5 / 2
print(f'Tipo de variável do resultado da divisão é: {type(divisao)}')


# Divisão - inteiro //
divisao_inteiro = 10 // 3
print(f'A divisão inteira é: {divisao_inteiro}')

# Valor em inteiro
print(f'valor inteiro: {int(divisao)}')


# Valor em float
print(f'valor float: {float(divisao)}')


# Modulo % - resto da divisão
modulo = 5 % 2
print(f'O valor do modulo é: {modulo}')

