# Condicionais
if True:
    print("Bloco IF vai ser executado ")
else:
    print("Bloco ELSE não será executado")


# Operadores lógicos and = "E" e or = "OU"  not = "NÃO" 

# and - Retorna True somente se todas as condições forem verdadeiras.

# or - Retorna True se pelo menos uma das condições for verdadeira.

# not - Inverte o valor lógico (onde for True ficará False e onde for False ficará True)


# Operador and

if True and True:
    print("Bloco será executado")

if True and False:
    print("Bloco não será executado") 

if False and False:
    print("Bloco não será executado")


# Operador OR
if True or false:
    print("Bloco OR vai ser executado")

if False or False:
    print("Bloco OR não vai ser executado")

if True or True:
    print("Bloco OR vai ser executado")

    


